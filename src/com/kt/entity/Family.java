package com.kt.entity;


public class Family {

  private int familyId;
  private String familyName;
  private String familyAction;
  private String familySex;
  private int familyAge;
  private String familyBorn;
  private String familyPhone;
  private String familyAddress;
  private String familyForm;
  private String familyJob;


  public int getFamilyId() {
    return familyId;
  }

  public void setFamilyId(int familyId) {
    this.familyId = familyId;
  }


  public String getFamilyName() {
    return familyName;
  }

  public void setFamilyName(String familyName) {
    this.familyName = familyName;
  }


  public String getFamilyAction() {
    return familyAction;
  }

  public void setFamilyAction(String familyAction) {
    this.familyAction = familyAction;
  }


  public String getFamilySex() {
    return familySex;
  }

  public void setFamilySex(String familySex) {
    this.familySex = familySex;
  }


  public int getFamilyAge() {
    return familyAge;
  }

  public void setFamilyAge(int familyAge) {
    this.familyAge = familyAge;
  }


  public String getFamilyBorn() {
    return familyBorn;
  }

  public void setFamilyBorn(String familyBorn) {
    this.familyBorn = familyBorn;
  }


  public String getFamilyPhone() {
    return familyPhone;
  }

  public void setFamilyPhone(String familyPhone) {
    this.familyPhone = familyPhone;
  }


  public String getFamilyAddress() {
    return familyAddress;
  }

  public void setFamilyAddress(String familyAddress) {
    this.familyAddress = familyAddress;
  }


  public String getFamilyForm() {
    return familyForm;
  }

  public void setFamilyForm(String familyForm) {
    this.familyForm = familyForm;
  }


  public String getFamilyJob() {
    return familyJob;
  }

  public void setFamilyJob(String familyJob) {
    this.familyJob = familyJob;
  }

}
